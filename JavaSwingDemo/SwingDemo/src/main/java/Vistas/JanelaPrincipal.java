    private void adicionarBotaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_adicionarBotaoMouseClicked
        try{
            String nomeCao = nomeTextField.getText();
            String racaCao = racaTextField.getText();
            String corCao = corTextField.getText();
            int idadeCao = Integer.valueOf(idadeTextField.getText());
            Cao c1 = new Cao(nomeCao, idadeCao, racaCao, corCao);
            System.out.println(nomeCao);
            caes.add(c1);
            StringBuilder str = new StringBuilder();
            for (Cao c : caes){
                str.append(c.getInformacao());
            }
            caesTextArea.setText(str.toString());
        }catch(NumberFormatException e){
            erroIdade.pack();
            erroIdade.setVisible(true);
        }
    }//GEN-LAST:event_adicionarBotaoMouseClicked

